    private void tbl_bookDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_bookDetailsMouseClicked
        // TODO add your handling code here:
        int rowNumber = tbl_bookDetails.getSelectedRow();
    	TableModel model = tbl_bookDetails.getModel();
    	int reference = Integer.parseUnsignedInt(model.getValueAt(rowNumber, 0).toString());
    	
    	DatabaseBook dbB = new DatabaseBook();
    	Book book = dbB.selectABook(reference);
    	
    	txt_bookReference.setText(String.valueOf(book.getReference()));
    	txt_bookName.setText( book.getName());
    	txt_authorName.setText( book.getAuthor());
    	textField_type.setText(book.typeBook(book.getType()));
    	txt_quantity.setText(String.valueOf(book.getQuantity()));
    	txtFiel_genre.setText( book.getTag());
    	TextField_publication.setText(String.valueOf(book.getPublication()));
    	textField_URL.setText(book.getUrl());
    	
    }//GEN-LAST:event_tbl_bookDetailsMouseClicked
