    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {  
    	DatabaseBook dbB = new DatabaseBook();
        // TODO add your handling code here:
    	if((!dbB.numberOrNot(TextField_publication.getText()))) {
    		JOptionPane.showMessageDialog(this, "Book Addition Unsuccessful");
    	}
    	if((!dbB.numberOrNot(txt_quantity.getText()))) {
    		JOptionPane.showMessageDialog(this, "Book Addition Unsuccessful");
    	}
    	if((!dbB.numberOrNot(txt_bookReference.getText()))) {
    		JOptionPane.showMessageDialog(this, "Book Addition Unsuccessful");
    	}
        if(dbB.addBook(Integer.parseInt(txt_bookReference.getText()),txt_bookName.getText(),txt_authorName.getText(), 
        		Integer.parseInt(txt_quantity.getText()), txtFiel_genre.getText(), Integer.parseInt(TextField_publication.getText()),
        		textField_URL.getText(), textField_type.getText())){
            JOptionPane.showMessageDialog(this, "Book Added");
            clearTable();
            setBookDetailsToTable();
        }
        else{
            JOptionPane.showMessageDialog(this, "Book Addition Unsuccessful");
        }
    }
    
